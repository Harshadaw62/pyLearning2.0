# Create a program that takes two numbers as input and prints whether the
# first number is greater than, less than, or equal to the second number.

num1 = input("Enter First number: ")
num2 = input("Enter Second number: ")
print("First number is Equal to Second number: ", num1 == num2)
print("First Number is not equal to Second Number: ", num1 != num2)
print("First Number is less than Second Number: ", num1 < num2)
print("First Number is greater than Second Number: ", num1 > num2)
